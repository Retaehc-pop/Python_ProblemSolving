import numpy as np
import matplotlib.pyplot as plt
import time

plt.xlabel('X')
plt.ylabel('Y')
data = {'a': np.arange(50),
        'c': np.random.randint(0, 50, 50),
        'd': np.random.randn(50)}
data['b'] = data['a'] + 10 * np.random.randn(50)
data['d'] = np.abs(data['d']) * 100

plt.scatter('a', 'b', c='c', s='d', data=data)
plt.plot('a', 'b', data=data)

plt.show()
